package exercicios.sala;

/**
 *
 * @author Kelvin Costa
 */
public class SalaDto {

    private int id;
    private double tempo;

    /**
     *
     * @return
     */
    public int getId() {
        return id;
    }

    /**
     *
     * @param id
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     *
     * @return
     */
    public double getTempo() {
        return tempo;
    }

    /**
     *
     * @param tempo
     */
    public void setTempo(double tempo) {
        this.tempo = tempo;
    }

}
